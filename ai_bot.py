def get_market_data():
    tickers = {
        "삼성전자": "005930.KS",
        "SK하이닉스": "000660.KS",
        "코스피(KOSPI)": "^KS11",
        "나스닥(NASDAQ)": "^IXIC",
        "원/달러 환율": "KRW=X",
        "WTI 유가": "CL=F"
    }
    market_info = "[실시간 주요 지표]\n"
    for name, ticker in tickers.items():
        try:
            data = yf.Ticker(ticker).history(period="1d")
            if not data.empty:
                current_price = data['Close'].iloc[0]
                market_info += f"- {name}: {current_price:,.2f}\n"
        except Exception:
            market_info += f"- {name}: 데이터 수집 일시 오류\n"
    return market_info

